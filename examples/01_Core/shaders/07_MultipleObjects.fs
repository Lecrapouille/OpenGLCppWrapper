#version 330 core

// ----------------------------------------------------------------------------
// Data from C++ program
// ----------------------------------------------------------------------------

// Texture sampler
uniform sampler2D texID;

// ----------------------------------------------------------------------------
// Data from vertex program
// ----------------------------------------------------------------------------
in struct v2f_s
{
   vec2 UV;         // Interpolated fragment texture coordinates
   vec4 color;      // Interpolated fragment color (out)
} v2f;

// ----------------------------------------------------------------------------
// Fragment program output
// ----------------------------------------------------------------------------
out vec4 fragColor;

// ----------------------------------------------------------------------------
// Fragment program
// ----------------------------------------------------------------------------
void main()
{
    // Final color
    fragColor = texture(texID, v2f.UV) * v2f.color;
}
