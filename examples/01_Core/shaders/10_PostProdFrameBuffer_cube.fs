// ----------------------------------------------------------------------------
// Data from C++ program
// ----------------------------------------------------------------------------

// Texture sampler
uniform sampler2D texID;

// ----------------------------------------------------------------------------
// Data from vertex program
// ----------------------------------------------------------------------------
in struct v2f_s
{
   // Interpolated fragment texture coordinates
   vec2 UV;
} v2f;

// ----------------------------------------------------------------------------
// Fragment program output
// ----------------------------------------------------------------------------
out vec4 fragColor;

// ----------------------------------------------------------------------------
// Fragment program
// ----------------------------------------------------------------------------
void main()
{
    // Final color
    fragColor = texture(texID, v2f.UV);
}
